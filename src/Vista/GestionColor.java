    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ArrayList<Color> coloresSetear = new ArrayList();
        
        if(this.jCheckBox1.isSelected()) coloresSetear.add(new Color(this.jCheckBox1.getText(), (int) this.jSpinner1.getValue()));
        if(this.jCheckBox2.isSelected()) coloresSetear.add(new Color(this.jCheckBox2.getText(), (int) this.jSpinner2.getValue()));
        if(this.jCheckBox3.isSelected()) coloresSetear.add(new Color(this.jCheckBox3.getText(), (int) this.jSpinner3.getValue()));
        if(this.jCheckBox4.isSelected()) coloresSetear.add(new Color(this.jCheckBox4.getText(), (int) this.jSpinner4.getValue()));
        if(this.jCheckBox5.isSelected()) coloresSetear.add(new Color(this.jCheckBox5.getText(), (int) this.jSpinner5.getValue()));
        if(this.jCheckBox6.isSelected()) coloresSetear.add(new Color(this.jCheckBox6.getText(), (int) this.jSpinner6.getValue()));
        if(this.jCheckBox7.isSelected()) coloresSetear.add(new Color(this.jCheckBox7.getText(), (int) this.jSpinner7.getValue()));
        if(this.jCheckBox8.isSelected()) coloresSetear.add(new Color(this.jCheckBox8.getText(), (int) this.jSpinner8.getValue()));

        
        for(Version version: controlador.getListas().getListaDeVersiones()){
          if(version.getModelo().getMarca().getMarca().equals(this.jComboBox1.getSelectedItem())){
              if(version.getModelo().getModelo().equals(this.jComboBox2.getSelectedItem())){
                  if(version.getDescripcion().equals(this.jComboBox3.getSelectedItem())){
                    int indiceVersion = controlador.getListas().getListaDeVersiones().indexOf(version);
                    controlador.getListas().getListaDeVersiones().get(indiceVersion).setColores(coloresSetear);
                    JOptionPane.showConfirmDialog(null, "El/los color/es se modificaron correctamente.",
                    "Color/es modificado/s", JOptionPane.CLOSED_OPTION,
                    JOptionPane.INFORMATION_MESSAGE);
                    break;
                  }
              }
          }
        }
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

