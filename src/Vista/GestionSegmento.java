    private void teclas(){
        this.addKeyListener(new KeyAdapter(){
            @Override
            public void keyPressed(KeyEvent e){
                int id;
                int keyCode = e.getKeyCode();
                if(keyCode == KeyEvent.VK_1 || keyCode == KeyEvent.VK_NUMPAD1){
                    id = 1;
                    presionar(id);
                }
                
                if(keyCode == KeyEvent.VK_2 || keyCode == KeyEvent.VK_NUMPAD2){
                    id = 2;
                    presionar(id);
                }
                
                if(keyCode == KeyEvent.VK_3 || keyCode == KeyEvent.VK_NUMPAD3){
                    id = 3;
                    presionar(id);
                }
            }
        });
    }
    
    private void presionar(int id){
        if(id == 1) this.jButton1.doClick();
        if(id == 2) this.jButton3.doClick();
        if(id == 3) this.jButton2.doClick();
    }
    
