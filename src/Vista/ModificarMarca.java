    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(this.jTextField1.getText().isBlank()){
            JOptionPane.showMessageDialog(rootPane, "Nombre no v치lido.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(this.jComboBox1.getSelectedItem() == null){
            JOptionPane.showMessageDialog(rootPane, "Marca seleccionada no v치lida", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(this.jComboBox1.getSelectedItem().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Marca seleccionada no v치lida", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        for(Marca marca: controlador.getListas().getListaDeMarcas()){
            if(marca.getMarca().equals(this.jComboBox1.getSelectedItem())){
                int indice = controlador.getListas().getListaDeMarcas().indexOf(marca);
                controlador.getListas().getListaDeMarcas().get(indice).setMarca(this.jTextField1.getText());
                JOptionPane.showConfirmDialog(null, "La marca se modific칩 correctamente.",
                "Marca modificada", JOptionPane.CLOSED_OPTION,
                JOptionPane.INFORMATION_MESSAGE);
                dispose();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

