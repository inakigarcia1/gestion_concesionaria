    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        if(this.jComboBox1.getSelectedItem() == null){
            this.jComboBox2.setEnabled(false);
            this.jComboBox3.setEnabled(false);
            this.jComboBox4.setEnabled(false);
            this.jTextField1.setEnabled(false);
            this.jTextField2.setEnabled(false);
            this.jButton1.setEnabled(false);
            return;
        }
        if(this.jComboBox1.getSelectedItem().equals("")){
            this.jComboBox2.setEnabled(false);
            this.jComboBox3.setEnabled(false);
            this.jComboBox4.setEnabled(false);
            this.jTextField1.setEnabled(false);
            this.jTextField2.setEnabled(false);
            this.jButton1.setEnabled(false);
            return;
        }
        this.jComboBox2.removeAllItems();
        this.jComboBox2.addItem("");
        this.jComboBox2.setEnabled(true);
        for(Modelo modelo: controlador.getListas().getListaDeModelos()){
            if(modelo.getMarca().getMarca().equals(this.jComboBox1.getSelectedItem())){
                this.jComboBox2.addItem(modelo.getModelo());
            }
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

    private void jComboBox2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox2ActionPerformed
        if(this.jComboBox2.getSelectedItem() == null){
            this.jComboBox3.setEnabled(false);
            this.jComboBox4.setEnabled(false);
            this.jTextField1.setEnabled(false);
            this.jTextField2.setEnabled(false);
            this.jButton1.setEnabled(false);
            return;
        }
        if(this.jComboBox2.getSelectedItem().equals("")){
            this.jComboBox3.setEnabled(false);
            this.jComboBox4.setEnabled(false);
            this.jTextField1.setEnabled(false);
            this.jTextField2.setEnabled(false);
            this.jButton1.setEnabled(false);
            return;
        }
        this.jTextField1.setEnabled(true);
        this.jTextField2.setEnabled(true);

        this.jComboBox4.setEnabled(true);
        this.jButton1.setEnabled(false);
        
        this.jComboBox4.removeAllItems();
        this.jComboBox4.addItem("");
        for(Version versionLista: controlador.getListas().getListaDeVersiones()){
            if(versionLista.getModelo().getMarca().getMarca().equals(this.jComboBox1.getSelectedItem())){
                if(versionLista.getModelo().getModelo().equals(this.jComboBox2.getSelectedItem())){
                    this.jComboBox4.addItem(versionLista.getDescripcion());
                }
            }
        }
    }//GEN-LAST:event_jComboBox2ActionPerformed

