    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(this.jComboBox1.getSelectedItem().equals("") || this.jComboBox1.getSelectedItem() == null){
            JOptionPane.showMessageDialog(rootPane, "Marca seleccionada no válida", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(this.jComboBox2.getSelectedItem().equals("") || this.jComboBox2.getSelectedItem() == null){
            JOptionPane.showMessageDialog(rootPane, "Modelo seleccionado no válido", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(this.jComboBox4.getSelectedItem().equals("") || this.jComboBox4.getSelectedItem() == null){
            JOptionPane.showMessageDialog(rootPane, "Versión seleccionada no válida", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        boolean nuevaDesc = true;
        boolean nuevoPrecio = true;
        boolean nuevoSeg = true;

        if(this.jTextField1.getText().isBlank()) nuevaDesc = false;
        if(this.jTextField2.getText().isBlank()) nuevoPrecio = false;
        if(this.jComboBox3.getSelectedItem().equals("") || this.jComboBox3.getSelectedItem() == null) nuevoSeg = false;
        
        
        if(!nuevaDesc && !nuevoPrecio && !nuevoSeg){
            JOptionPane.showConfirmDialog(null, "No se realizó ninguna modificación.",
                                    "Sin modificación", JOptionPane.CLOSED_OPTION,
                                    JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        if(nuevoPrecio){
            for(int i = 0; i < this.jTextField2.getText().length(); i++){
                if(!Character.isDigit(this.jTextField2.getText().charAt(i))){
                    JOptionPane.showMessageDialog(rootPane, "Precio de versión no válido.", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }
        }
        
        for(Version versionLista: controlador.getListas().getListaDeVersiones()){
            if(versionLista.getModelo().getMarca().getMarca().equals(this.jComboBox1.getSelectedItem())){
                if(versionLista.getModelo().getModelo().equals(this.jComboBox2.getSelectedItem())){
                    int indice = controlador.getListas().getListaDeVersiones().indexOf(versionLista);
                    if(nuevaDesc){
                        String desc = this.jTextField1.getText();
                        controlador.getListas().getListaDeVersiones().get(indice).setDescripcion(desc);
                    }
                    if(nuevoPrecio){
                        double precio = Double.parseDouble(this.jTextField2.getText());
                        controlador.getListas().getListaDeVersiones().get(indice).setPrecioUnidad(precio);
                        controlador.getListas().getListaDeVersiones().get(indice).calcularPatentamiento();
                    }
                    if(nuevoSeg){
                        for(Segmento segmentoLista: controlador.getListas().getListaDeSegmentos()){
                            if(segmentoLista.getSegmento().equals(this.jComboBox3.getSelectedItem())){
                                controlador.getListas().getListaDeVersiones().get(indice).getSegmento().setSegmento(segmentoLista.getSegmento());
                                controlador.getListas().getListaDeVersiones().get(indice).getSegmento().setCostoFlete(segmentoLista.getCostoFlete());
                            }
                        }
                    }
                    JOptionPane.showConfirmDialog(null, "Versión modificada correctamente.",
                                    "Versión Modificada", JOptionPane.CLOSED_OPTION,
                                    JOptionPane.INFORMATION_MESSAGE);
                    break;
                }
            }
        }
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

