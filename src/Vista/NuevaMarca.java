    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(this.jTextField1.getText().isBlank()){
                JOptionPane.showMessageDialog(rootPane, "No se introdujo un nombre de marca.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
        }
        
        for(Marca marca: controlador.getListas().getListaDeMarcas()){
            if(marca.getMarca().equals(this.jTextField1.getText())){
                JOptionPane.showMessageDialog(rootPane, "Marca ya existente.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        controlador.getListas().getListaDeMarcas().add(new Marca(this.jTextField1.getText()));
        JOptionPane.showConfirmDialog(null, "La marca se agreg√≥ correctamente.",
                "Marca agregada", JOptionPane.CLOSED_OPTION,
                JOptionPane.INFORMATION_MESSAGE);
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

