    private void jTextField3KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField3KeyReleased
        if(evt.getKeyCode() == KeyEvent.VK_BACK_SPACE){
            return;
        }
        if(evt.getKeyCode() == KeyEvent.VK_SLASH){
            this.jTextField3.setText(this.jTextField3.getText().replace("/", ""));
            return;
        }
        if(this.jTextField3.getText().length() == 2){
            this.jTextField3.setText(this.jTextField3.getText() + "/");
        }
        if(this.jTextField3.getText().length() == 5){
            this.jTextField3.setText(this.jTextField3.getText() + "/");
        }
    }//GEN-LAST:event_jTextField3KeyReleased

    public void cerradoCliente(){
        String nombre;
        String apellido;
        String completo;
        nombre = this.controlador.getListas().getListaDeClientes().get(this.controlador.getListas().getListaDeClientes().size() - 1).getNombre();
        apellido = this.controlador.getListas().getListaDeClientes().get(this.controlador.getListas().getListaDeClientes().size() - 1).getApellido();
        completo = nombre + " " + apellido;

        if(!this.jComboBox5.getItemAt(this.jComboBox5.getItemCount() - 1).equals(completo)){
            this.jComboBox5.addItem(completo);
        }
    }
    
    public void cerradoVehiculo(Version versionN, Marca marcaN, boolean nuevaMarca, boolean nuevoModelo, boolean nuevaVersion){
        
        if(nuevaMarca){
            this.jComboBox1.addItem(marcaN.getMarca());
            this.jComboBox3.addItem(versionN.getModelo().getModelo());
            this.jComboBox2.addItem(versionN.getDescripcion());
        }
        else if (nuevoModelo){
            this.jComboBox3.addItem(versionN.getModelo().getModelo());
            this.jComboBox2.addItem(versionN.getDescripcion());
        }
        else if (nuevaVersion){
            this.jComboBox2.addItem(versionN.getDescripcion());
        }
        else{
            this.actualizarColores();
        }
    }
    
    private void actualizarColores(){
        this.jComboBox4.removeAllItems();
        this.jComboBox4.addItem("");
        
        for(Version version: controlador.getListas().getListaDeVersiones()){
            if(version.getDescripcion().equals(this.jComboBox3.getSelectedItem())){
                for(Color color: version.getColores()){
                    if(color.getCantidad() > 0){
                        this.jComboBox4.addItem(color.getColor());
                    }
                }
            }
        }
    }
    
