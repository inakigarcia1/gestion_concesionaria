    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String formatoFecha = this.jTextField3.getText();
        
        Fecha fecha = new Fecha();
        
        if(!fecha.formato(formatoFecha)){
            JOptionPane.showMessageDialog(rootPane, "Fecha incorrecta", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Marca marca = new Marca();
        Modelo modelo = new Modelo();
        Version version = new Version();
        Segmento segmento = new Segmento();
        Color color = new Color();
        Cliente cliente = new Cliente();
        
        for(Marca marcaDeLista: controlador.getListas().getListaDeMarcas()){
            if(marcaDeLista.getMarca().equals(this.jComboBox1.getSelectedItem())){
                marca.setMarca(marcaDeLista.getMarca());
                break;
            }
        }
        
        for(Modelo modeloDeLista: controlador.getListas().getListaDeModelos()){
            if(modeloDeLista.getModelo().equals(this.jComboBox2.getSelectedItem())){
                modelo.setMarca(marca);
                modelo.setModelo(modeloDeLista.getModelo());
                break;
            }
        }
        
        for(Version versionDeLista: controlador.getListas().getListaDeVersiones()){
            if(versionDeLista.getModelo().getMarca().getMarca().equals(this.jComboBox1.getSelectedItem())){
                if(versionDeLista.getModelo().getModelo().equals(this.jComboBox2.getSelectedItem())){
                    if(versionDeLista.getDescripcion().equals(this.jComboBox3.getSelectedItem())){
                        version.setDescripcion(versionDeLista.getDescripcion());
                        version.setModelo(versionDeLista.getModelo());
                        version.setPrecioUnidad(versionDeLista.getPrecioUnidad());
                        version.setSegmento(versionDeLista.getSegmento());
                        version.setColores(versionDeLista.getColores());
//                        version.calcularPatentamiento();
                        break;
                    }
                }
            }  
        }
        
        for(Segmento segmentoDeLista: controlador.getListas().getListaDeSegmentos()){
            if(segmentoDeLista.getSegmento().equals(version.getSegmento().getSegmento())){
                segmento.setCostoFlete(segmentoDeLista.getCostoFlete());
                segmento.setSegmento(segmentoDeLista.getSegmento());
                segmento.setPatentamiento(version.getSegmento().getPatentamiento());
                break;
            }
        }
        
        for(Version versionL: controlador.getListas().getListaDeVersiones()){
            if(versionL.getDescripcion().equals(this.jComboBox3.getSelectedItem())){
                for(Color colorL: version.getColores()){
                    if(colorL.getColor().equals(this.jComboBox4.getSelectedItem())){
                        color.setColor(colorL.getColor());
                        color.setCantidad(colorL.getCantidad());
                    }
                }
            }
         }
        
        String nombre;
        
        for(Cliente clienteLista: controlador.getListas().getListaDeClientes()){
            nombre = clienteLista.getNombre() + " " + clienteLista.getApellido();
            if(nombre.equals(this.jComboBox5.getSelectedItem())){
                cliente.setApellido(clienteLista.getApellido());
                cliente.setCel((long) clienteLista.getCel());
                cliente.setDNI(clienteLista.getDNI());
                cliente.setDireccion(clienteLista.getDireccion());
                cliente.setEmail(clienteLista.getEmail());
                cliente.setNombre(clienteLista.getNombre());
                cliente.setTel((long) clienteLista.getTel());
                break;
            }
        }
        
        Reserva reservaTemp = new Reserva();
        
        reservaTemp.setCliente(cliente);
        reservaTemp.setMarca(marca);
        reservaTemp.setModelo(modelo);
        reservaTemp.setVersion(version);
        reservaTemp.setSegmento(segmento);
        reservaTemp.setColor(color);
        reservaTemp.setFecha(fecha);
        reservaTemp.calcularPrecioFinal();
        
        String detalle=String.format("Cliente: %s %s\n"
                               + "Fecha: %s\n"
                               + "Marca: %s\n"
                               + "Modelo: %s\n"
                               + "Version: %s\n"
                               + "Color: %s\n"
                               + "Segmento: %s\n"
                               + "Precio Unidad: $ %.2f\n"
                               + "Precio Flete: $ %.2f\n"
                               + "Precio Patentamiento: $ %.2f\n"
                               + "TOTAL: $ %.2f\n\n",
                cliente.getApellido(), cliente.getNombre(), fecha.toString(), marca.getMarca(),
                modelo.getModelo(), version.getDescripcion(), color.getColor(), segmento.getSegmento(),
                version.getPrecioUnidad(), segmento.getCostoFlete(), segmento.getPatentamiento(),
                reservaTemp.getPrecioFinal()); 

        int a = JOptionPane.showConfirmDialog(null, detalle + "¿Desea confirmar la reserva?", 
                "Confirmar Reserva", JOptionPane.YES_NO_OPTION, 
		JOptionPane.INFORMATION_MESSAGE); 
        
        if(a == JOptionPane.YES_OPTION){
            reservaTemp.setEstado("Pendiente");
            controlador.getListas().getListaDeReservas().add(reservaTemp);
            for(Version versionDeLista: controlador.getListas().getListaDeVersiones()){
                if(versionDeLista.getDescripcion().equals(this.jComboBox3.getSelectedItem())){
                    for(Color colorLista: versionDeLista.getColores()){
                        if(colorLista.getColor().equals(this.jComboBox4.getSelectedItem())){
                            int indice1 = controlador.getListas().getListaDeVersiones().indexOf(versionDeLista);
                            int indice2 = controlador.getListas().getListaDeVersiones().get(indice1).getColores().indexOf(colorLista);
                            controlador.getListas().getListaDeVersiones().get(indice1).getColores().get(indice2).setCantidad(
                            controlador.getListas().getListaDeVersiones().get(indice1).getColores().get(indice2).getCantidad() - 1);
                        }
                    }
                }
            }
            
            JOptionPane.showConfirmDialog(null, "La reserva se almacenó correctamente.",
                "Rerseva almacenada", JOptionPane.CLOSED_OPTION,
                JOptionPane.INFORMATION_MESSAGE);
            
            this.jComboBox5.setSelectedIndex(0);
            this.jComboBox4.setSelectedIndex(0);
            this.jComboBox3.setSelectedIndex(0);
            this.jComboBox2.setSelectedIndex(0);
            this.jComboBox1.setSelectedIndex(0);
            this.jTextField3.setText("");
        }
        if(a == JOptionPane.NO_OPTION){
            reservaTemp.setEstado("Rechazado");
            controlador.getListas().getListaDeReservas().add(reservaTemp);
            JOptionPane.showConfirmDialog(null, "La reserva ha sido rechazada.",
                "Rerseva rechazada", JOptionPane.CLOSED_OPTION,
                JOptionPane.INFORMATION_MESSAGE);
            this.jComboBox5.setSelectedIndex(0);
            this.jComboBox4.setSelectedIndex(0);
            this.jComboBox3.setSelectedIndex(0);
            this.jComboBox2.setSelectedIndex(0);
            this.jComboBox1.setSelectedIndex(0);
            this.jTextField3.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

