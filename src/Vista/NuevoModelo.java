    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(this.jTextField1.getText().isBlank()){
            JOptionPane.showMessageDialog(rootPane, "Nombre no v치lido.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(this.jComboBox1.getSelectedItem() == null){
            JOptionPane.showMessageDialog(rootPane, "Marca seleccionada no v치lida", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(this.jComboBox1.getSelectedItem().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Marca seleccionada no v치lida", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        
        for(Modelo modeloLista: controlador.getListas().getListaDeModelos()){
            if(modeloLista.getModelo().equals(this.jTextField1.getText())){
                if(modeloLista.getMarca().getMarca().equals(this.jComboBox1.getSelectedItem())){
                    JOptionPane.showMessageDialog(rootPane, "Modelo ya existente en " + modeloLista.getMarca().getMarca() + ".", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }
        }
        
        for(Marca marcaLista: controlador.getListas().getListaDeMarcas()){
            if(marcaLista.getMarca().equals(this.jComboBox1.getSelectedItem())){
                Modelo modelo = new Modelo(marcaLista, this.jTextField1.getText());
                controlador.getListas().getListaDeModelos().add(modelo);
                JOptionPane.showConfirmDialog(null, "El modelo se agreg칩 correctamente.",
                "Modelo agregado", JOptionPane.CLOSED_OPTION,
                JOptionPane.INFORMATION_MESSAGE);
                dispose();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

