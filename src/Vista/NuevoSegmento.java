    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(this.jTextField1.getText().isBlank()){
                JOptionPane.showMessageDialog(rootPane, "No se introdujo un segmento.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
        }
        
        if(this.jTextField2.getText().isBlank()){
                JOptionPane.showMessageDialog(rootPane, "No se introdujo un precio de flete.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
        }
        
        for(int i = 0; i < this.jTextField2.getText().length(); i++){
            if(!Character.isDigit(this.jTextField2.getText().charAt(i))){
                JOptionPane.showMessageDialog(rootPane, "Precio de flete no válido.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        
        for(Segmento segmentoLista: controlador.getListas().getListaDeSegmentos()){
            if(segmentoLista.getSegmento().equals(this.jTextField1.getText())){
                JOptionPane.showMessageDialog(rootPane, "Segmento ya existente.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        controlador.getListas().getListaDeSegmentos().add(new Segmento(this.jTextField1.getText(), Double.parseDouble(this.jTextField2.getText())));
        JOptionPane.showConfirmDialog(null, "El segmento se agregó correctamente.",
                "Segmento agregado", JOptionPane.CLOSED_OPTION,
                JOptionPane.INFORMATION_MESSAGE);
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

