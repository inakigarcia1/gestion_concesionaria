            String DNI = Registro.this.jTextField3.getText();
            String Cel = Registro.this.jTextField4.getText();
            String Tel = Registro.this.jTextField5.getText();
            String Email = Registro.this.jTextField6.getText();
            String Direccion = Registro.this.jTextField7.getText();
