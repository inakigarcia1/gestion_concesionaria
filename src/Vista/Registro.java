    private boolean revisarDatos(String nombre, String apellido, String dni, String direccion, String email, String cel, String tel){
        if(nombre.isBlank()) return false;
        if(apellido.isBlank()) return false;
        if(dni.isBlank()) return false;
        if(direccion.isBlank()) return false;
        if(email.isBlank()) return false;
        if(cel.isBlank()) return false;
        if(tel.isBlank()) return false;
        
        if(!tieneSoloLetras(nombre)) return false;
        if(!tieneSoloLetras(apellido)) return false;
        
        if(!tieneSoloNumeros(dni)) return false;
        if(dni.length() != 8) return false;
        if(!tieneSoloNumeros(cel)) return false;
        if(!tieneSoloNumeros(tel)) return false;
        
        if(!email.contains("@")) return false;
        if(!email.endsWith(".com")) return false;
        
        return true;
    }
    
    private boolean tieneSoloLetras(String cadena){
        for(int i = 0; i < cadena.length(); i++){
            if(!Character.isLetter(cadena.charAt(i))) return false;
        }
        return true;
    }
    
    private boolean tieneSoloNumeros(String cadena){
        for(int i = 0; i < cadena.length(); i++){
            if(!Character.isDigit(cadena.charAt(i))) return false;
        }
        return true;
    }
    
